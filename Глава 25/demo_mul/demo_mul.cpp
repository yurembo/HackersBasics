#include <iostream>

int main()
{
	int a = 2;
	std::cout << a * 16 << " " << a * 4 + 5 << " " << a * 13 << std::endl;
	double d = 1.5;
	std::cout << d * 3 << " " << d * 5 + a << " " << std::endl;
	float f = 2.2f;
	std::cout << f * 3 << std::endl;
}
